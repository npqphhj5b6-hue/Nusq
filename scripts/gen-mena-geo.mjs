// Build-time generator: projects the Arab-world countries with a d3-geo Mercator
// projection (matching the Nusq.dc.html prototype) and bakes the resulting SVG
// path strings + capital pixel coordinates into src/lib/mena-geo.ts. Run with:
//   node scripts/gen-mena-geo.mjs
import { readFileSync, writeFileSync } from "fs";
import { geoMercator, geoPath } from "d3-geo";
import { feature, merge } from "topojson-client";
import { createRequire } from "module";
const require = createRequire(import.meta.url);
const topo = require("world-atlas/countries-50m.json");

const WIDTH = 560, HEIGHT = 300;

// ISO-3166-1 numeric codes for the Arab League / Arabic-speaking countries,
// mapped to the English country name our data uses (matches MenaMap GEO_LOOKUP).
const CODE_TO_NAME = {
  "012": "Algeria", "048": "Bahrain", "818": "Egypt", "368": "Iraq",
  "400": "Jordan", "414": "Kuwait", "422": "Lebanon", "434": "Libya",
  "478": "Mauritania", "504": "Morocco", "512": "Oman", "275": "Palestine",
  "634": "Qatar", "682": "Saudi Arabia", "729": "Sudan", "760": "Syria",
  "788": "Tunisia", "784": "United Arab Emirates", "887": "Yemen", "760b": "Syria",
};
// Morocco + Western Sahara merged so no internal border shows.
const MERGED = [{ id: "504", parts: ["504", "732"] }];

// Capital coordinates [lng, lat] for pin placement (superset of story locations).
const CAPITALS = {
  "Saudi Arabia": [46.6753, 24.7136], "Riyadh": [46.72, 24.69],
  "United Arab Emirates": [54.37, 24.47], "UAE": [54.37, 24.47],
  "Dubai": [55.27, 25.2], "Abu Dhabi": [54.37, 24.47],
  "Qatar": [51.531, 25.2854], "Doha": [51.53, 25.29],
  "Kuwait": [47.9774, 29.3759], "Kuwait City": [47.98, 29.37],
  "Bahrain": [50.56, 26.07], "Manama": [50.59, 26.22],
  "Oman": [58.59, 23.59], "Muscat": [58.59, 23.59],
  "Egypt": [31.2357, 30.0444], "Cairo": [31.25, 30.06],
  "Jordan": [35.93, 31.95], "Amman": [35.93, 31.95],
  "Iraq": [44.4, 33.34], "Baghdad": [44.4, 33.34],
  "Lebanon": [35.5, 33.89], "Beirut": [35.5, 33.89],
  "Syria": [36.29, 33.51], "Libya": [13.18, 32.9], "Tripoli": [13.18, 32.9],
  "Tunisia": [10.17, 36.82], "Tunis": [10.17, 36.82],
  "Algeria": [3.06, 36.74], "Algiers": [3.06, 36.74],
  "Morocco": [-6.83, 34.01], "Rabat": [-6.83, 34.01], "Casablanca": [-7.62, 33.59],
  "Sudan": [32.53, 15.55], "Khartoum": [32.53, 15.55],
  "Yemen": [44.19, 15.35], "Mauritania": [-15.98, 18.08],
  "Palestine": [35.2, 31.9],
};

const allGeoms = topo.objects.countries.geometries;
const wanted = new Set(Object.keys(CODE_TO_NAME).filter(c => c.length === 3));
const mergedIds = new Set(MERGED.flatMap(g => g.parts));

const plain = allGeoms
  .filter(g => wanted.has(String(g.id)) && !mergedIds.has(String(g.id)))
  .map(g => feature(topo, g));
const mergedFeatures = MERGED.map(group => ({
  type: "Feature", id: group.id,
  geometry: merge(topo, allGeoms.filter(g => group.parts.includes(String(g.id)))),
}));
const features = [...plain, ...mergedFeatures];

const projection = geoMercator().fitExtent(
  [[18, 18], [WIDTH - 18, HEIGHT - 18]],
  { type: "FeatureCollection", features }
);
const path = geoPath(projection);
const round = (d) => d.replace(/-?\d+\.\d+/g, (n) => (+n).toFixed(1));

const countries = features.map(f => ({
  id: String(f.id),
  name: CODE_TO_NAME[String(f.id)] ?? String(f.id),
  d: round(path(f)),
})).filter(c => c.d);

const capitalPx = {};
for (const [k, lnglat] of Object.entries(CAPITALS)) {
  const p = projection(lnglat);
  if (p) capitalPx[k] = { x: +p[0].toFixed(1), y: +p[1].toFixed(1) };
}

const out = `// AUTO-GENERATED by scripts/gen-mena-geo.mjs — do not edit by hand.
// d3-geo Mercator projection of the Arab-world countries, baked to SVG paths so
// the briefing map needs no d3/topojson/fetch at runtime. Regenerate with:
//   node scripts/gen-mena-geo.mjs
export const MENA_MAP_VIEWBOX = "0 0 ${WIDTH} ${HEIGHT}";

export interface MenaCountryPath { id: string; name: string; d: string; }
export const MENA_COUNTRY_PATHS: MenaCountryPath[] = ${JSON.stringify(countries, null, 2)};

export const MENA_CAPITAL_PX: Record<string, { x: number; y: number }> = ${JSON.stringify(capitalPx, null, 2)};
`;
writeFileSync("src/lib/mena-geo.ts", out);
console.log(`✓ wrote src/lib/mena-geo.ts — ${countries.length} countries, ${Object.keys(capitalPx).length} capitals`);
console.log("countries:", countries.map(c => c.name).join(", "));
console.log("approx size:", (out.length / 1024).toFixed(1), "KB");
